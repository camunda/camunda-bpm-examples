package org.camunda.bpm.example.spin.dataformat.servlet;

import java.lang.annotation.ElementType;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

import jakarta.interceptor.InterceptorBinding;

/**
 * Annotation that is used to declare methods that should be executed in
 * process application context (implemented via {@link ProcessApplicationContextInterceptor}).
 *
 * @author Thorben Lindhauer
 */
@Inherited
@InterceptorBinding
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.METHOD, ElementType.TYPE})
public @interface InProcessApplicationContext {

}
